# Store immutable student IDs using a tuple
student_IDs = ("CS023", "CS001", "CS042", "CS077", "CS056")

# Create a set of unique course names
courses = {"Python", "AI", "ML", "Data Science", "Computer Science"}

# Add a new course to the set
courses.add("Cyber Security")

# Remove an existing course from the set
courses.remove("AI")

# Print the updated set of courses
print(courses)

# Print the tuple of student IDs
print(student_IDs)
